    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($account_no)
    {
        // dd($request);
        $account = auth()->user()->accounts->firstWhere('account_no',$account_no);
        if(!is_null(request()->input('query'))){
        return view('accounts/show')->with('account', $account)->with('request',request());
        }
        return view('accounts/show')->with('account', $account)->with('request',collect(['query'=>""]));
    }
